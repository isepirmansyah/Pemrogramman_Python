print('=======Cetak bilangan 10 - 15==========')
bil = 10
while(bil <= 15):
    print('Bilangan =' ,bil)
    bil += 1

print('=======Cetak bilangan 10 - 1==========')
bil = 10
while(bil >= 1):
    print('Bilangan =' ,bil)
    bil -= 1

print('=======Cetak bilangan genap diantara 10 - 15==========')
bil = 10
while(bil <= 15):
    if(bil % 2 == 0):
        print('Bilangan =' ,bil)
    bil += 1

print('=======Cetak bilangan ganjil diantara 10 - 15==========')
bil = 10
while(bil <= 15):
    if(bil % 2 == 1):
        print('Bilangan =' ,bil)
    bil += 1